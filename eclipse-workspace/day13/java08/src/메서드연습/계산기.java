package 메서드연습;

public class 계산기 {
	public void sum(int x, int y) {
		System.out.println(x+y);
	}
	
	public int subtract(int x, int y) {
		return (x-y);
	}
	
	public void mult(int x, int y) {
		System.out.println(x*y);
	}
	
	public void divide(int x, int y) {
		System.out.println((double)x/y);
	}
	
}
