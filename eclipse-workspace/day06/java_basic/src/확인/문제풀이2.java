package 확인;

public class 문제풀이2 {

	public static void main(String[] args) {
		
		int num1 =30;
		int num2 =40;
		
		int sum = num1+num2;
		int div = num1/num2;
		int rem = num1%num2;

		System.out.println("두수를 더하면 얼마? "+sum);
		System.out.println("num1에서 num2를 나누면 얼마? "+div);
		System.out.println("num1에서 num2를 나눈 나머지는 얼마? "+rem);
	}

}
